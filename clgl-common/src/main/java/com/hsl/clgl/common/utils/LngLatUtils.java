public class LngLatUtils {
    /**
     * 经度 纬度转为json字符串
     * @param lng
     * @param lat
     * @return
     */
    public static String getJson(String lng,String lat){
        return "{\"lng\":"+lng+",\"lat\":"+lat+"}";
    }
}
